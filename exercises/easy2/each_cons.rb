=begin
Write a method called each_cons that behaves similarly for Arrays, taking the
elements 2 at a time. The method should take an Array as an argument, and a block.
It should yield each consecutive pair of elements to the block, and return nil.

Your method may use #each, #each_with_object, #each_with_index, #inject, loop,
for, while, or until to iterate through the Array passed in as an argument, but
must not use any other methods that iterate through an Array or any other
collection.
=end

def each_cons(arr, num_elements)
  count = 0
  loop do
    break if (count + num_elements) > arr.size
    yield(*arr[count..(count + num_elements - 1)])
    count += 1
  end
  nil
end

hash = {}
each_cons([1, 3, 6, 10], 1) do |value|
  hash[value] = true
end
p hash == { 1 => true, 3 => true, 6 => true, 10 => true }

